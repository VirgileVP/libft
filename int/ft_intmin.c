#include "libft.h"

int		ft_intmin(int *tab)
{
	int	min;

	min = *tab;
	if (*tab)
	{
		while (*tab)
		{
			if (*tab < min)
				min = *tab;
			tab++;
		}
		return (min);
	}
	return (0);
}
