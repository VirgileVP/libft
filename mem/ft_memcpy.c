#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	unsigned char	*mem;

	mem = dst;
	while (n--)
		*mem++ = *(unsigned char*)src++;
	return (dst);
}
